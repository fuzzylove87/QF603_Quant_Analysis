function Incometax(bi, mi, xi, x)
if x>xi(end)
    i=length(xi)
else
    c=find(xi>x);
    i=c(1)-1;
end

if i==0
    y=0
else
    y=bi(i)+mi(i)/100*(x-xi(i))
end
end
